# Bit swap mapping (index-based: 0 = LSB, 7 = MSB)
swap_map = {
    4: 7,  # 16 -> 128
    7: 6,  # 128 -> 64
    6: 5,  # 64 -> 32
    5: 4,  # 32 -> 16
    0: 3,  # 1 -> 8
    3: 2,  # 8 -> 4
    2: 1,  # 4 -> 2
    1: 0   # 2 -> 1
}

def swap_bits(bin_str):
    bits = list(bin_str[::-1])  # reverse for LSB index
    new_bits = bits.copy()
    
    for src, dst in swap_map.items():
        new_bits[dst] = bits[src]
    
    return ''.join(new_bits[::-1])

